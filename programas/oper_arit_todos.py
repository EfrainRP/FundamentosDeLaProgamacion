#Efrain Robles Pulido
#Entrada
n1= int(input("Dame un número: "))
n2= int(input("Dame otro número: "))

#Proceso
s=n1+n2
r=n1-n2
m=n1*n2
d=n1/n2
de=n1//n2
rd=n1%n2
p=n1**n2

#Salida
print("El resultado de la suma es = ",s)
print ("El resultado de la resta es = ", r)
print ("El resultado de la multiplicación es = ", m)
print ("El resultado de la división es = ", d)
print ("El resultado de la división entera es = ", de)
print ("El residuo de la división es = ", rd)
print ("El resultado de la potencia es = ", p)

