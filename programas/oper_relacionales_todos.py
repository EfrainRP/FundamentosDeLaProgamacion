#Programa donde se utiliza los operadores relacionales y la selectiva simple
#Entrada
d1= int(input("Introduce un dato:"))
d2= int(input("Introduce otro dato:"))

#Proceso
if d1 > d2:
    print (d1," es mayor a ",d2) #Salida
if d1 < d2:
    print (d1," es menor a ",d2) #Salida
if d1 >= d2:
    print (d1," es mayor o igual a ",d2) #Salida
if d1 <= d2:
    print (d1," es menor o igual a ",d2) #Salida
if d1 == d2:
    print (d1," es igual a ",d2) #Salida
if d1 != d2:
    print (d1," es diferente a ",d2) #Salida
